package main

import "fmt"

func main() {
	fmt.Println("------------------------------------------------")
	fmt.Println("Uso de Println")
	fmt.Println("Nome: Rodrigo L. Alves")
	fmt.Println("Idade: 44 anos")
	fmt.Println("Cidade Natal: São Paulo")
	fmt.Println("------------------------------------------------")
	fmt.Println("")
	fmt.Println("------------------------------------------------")
	fmt.Printf("Uso de %s | ", "Printf")
	fmt.Printf("Nome: %s | ", "Rodrigo L. Alves")
	fmt.Printf("Idade: %d anos | ", 44)
	fmt.Printf("Cidade Natal: %s | ", "São Paulo")
	fmt.Println("")
	fmt.Println("------------------------------------------------")
}
