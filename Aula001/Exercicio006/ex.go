package main

import (
	"fmt"
)

func main() {

	valor1 := 10
	valor2 := 25

	fmt.Printf("==: %v \n", valor1 == valor2)
	fmt.Printf("!=: %v \n", valor1 != valor2)
	fmt.Printf("<: %v \n", valor1 < valor2)
	fmt.Printf(">: %v \n", valor1 > valor2)
	fmt.Printf("<=: %v \n", valor1 <= valor2)
	fmt.Printf(">=: %v \n", valor1 >= valor2)
}
