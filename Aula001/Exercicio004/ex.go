package main

import (
	"fmt"
)

func main() {

	v1 := 50
	v2 := 25

	fmt.Printf("Variavel soma: %d \n", v1+v2)
	fmt.Printf("Variavel subtracao: %d \n", v1-v2)
	fmt.Printf("Variavel produto: %d \n", v1*v2)
	fmt.Printf("Variavel quociente: %d \n", v1/v2)
	fmt.Printf("Variavel resto: %d \n", v1%v2)
}
